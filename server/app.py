# Global chat engine
engine: ChatEngine | None = None
moderation_provider = None


@asynccontextmanager
async def lifespan(app: FastAPI):
    """Initialize DB and providers on startup."""
    global engine, moderation_provider

    logger.info("Initializing database...")
    await init_db()

    logger.info("Loading persona...")
    persona = load_persona(PERSONA_FILE_SEXTING)
    nsfw_persona = persona  # one always-open persona drives everything
    logger.info("Persona loaded: %s", persona.name)

    logger.info("Initializing LLM providers...")
    # Mia is always-open, so every reply is generated by Grok (nsfw_provider).
    nsfw_provider = GrokProvider()
    classifier_provider = GeminiProvider()
    # thinking_budget=0 → no "thinking" latency; this provider only writes short
    # replies (suggestions + Grok fallback), so speed matters more than reasoning.
    fallback_provider = GeminiProvider(GEMINI_FALLBACK_MODEL, thinking_budget=0)
    vision_provider = GrokProvider()
    # Input moderation: Grok, not Gemini. Gemini's non-configurable
    # PROHIBITED_CONTENT filter refuses to even classify the very content we
    # need to detect (bestiality, CSAM, etc.), returning an empty response.
    # Grok reliably classifies it and still distinguishes adult roleplay.
    moderation_provider = GrokProvider()

    engine = ChatEngine(
        persona=persona,
        nsfw_persona=nsfw_persona,
        nsfw_provider=nsfw_provider,
        classifier_provider=classifier_provider,
        vision_provider=vision_provider,
        fallback_provider=fallback_provider,
    )
    logger.info("Chat engine ready")

    yield

    logger.info("Shutting down")


app = FastAPI(title="AI Girlfriend Chat", lifespan=lifespan)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

