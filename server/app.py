async def _generate_dynamic_opening(engine: ChatEngine) -> str:
    """Ask the LLM to write Mia's opening message — fresh every time.

    Includes the same time-of-day / location context that build_prompt injects
    into every reply, so the opening is consistent with where she'll say she is
    once he answers. Without it the bare persona defaults to a generic vibe
    that then contradicts the first time-aware reply.
    """
    from bot.time_context import get_time_prompt
    persona_prompt = engine.persona.to_system_prompt()
    time_prompt = await get_time_prompt()
    messages = [
        {
            "role": "system",
            "content": (
                f"{persona_prompt}\n\n"
                f"{time_prompt}\n\n"
                "You are starting a conversation with him for the first time. "
                "Write your opening message — the thing you finally worked up "
                "the nerve to say after wanting to since Tyler's birthday party. "
                "Ground it in where you actually are and the time of day RIGHT NOW (above) — never "
                "reference a different place or time. 2-3 short texts maximum, "
                "each on its own line. No period at the end. Be natural, be real, "
                "be yourself — crude, shameless, flirty. Do NOT use the same phrasing as your "
                "opening_lines — say something fresh."
            ),
        },
        {"role": "user", "content": "(he hasn't said anything yet — you're texting first)"},
    ]
    return await engine.nsfw_provider.generate(messages)


@app.get("/api/history/{mode}")
async def get_history(mode: str, user_id: int = Query(default=None)):
    """Return chat history for a specific mode. Opening message is sent via WebSocket."""
    user_id = user_id or DEFAULT_USER_ID
    if mode != "sexting":
        return JSONResponse({"error": "Invalid mode"}, status_code=400)

    messages = await get_all_messages(user_id, mode=mode)

    out = []
    for m in messages:
        if m["role"] not in ("user", "assistant"):
            continue
        img = m.get("image_url")
        entry = {
            "role": m["role"],
            "content": m["content"],
            "timestamp": m["timestamp"],
            "image_url": img,
        }
        out.append(entry)

    return {"mode": mode, "messages": out}


@app.post("/api/suggest")
async def suggest_reply(user_id: int = Query(default=None), mode: str = Query(default="sexting")):
    """AI Help — generate a suggested reply for the user to send to Mia."""
    if not engine:
        return JSONResponse({"error": "Engine not ready"}, status_code=503)
    uid = user_id or DEFAULT_USER_ID
    try:
        suggestion = await engine.suggest_reply(uid, mode=mode)
    except Exception as e:
        logger.warning("Suggest endpoint failed: %s", e)
        suggestion = ""
    return {"suggestion": suggestion}


@app.post("/api/reset")
async def reset_user(user_id: int = Query(default=None)):
    """Wipe all data for a user — messages, memories, facts, engagement."""
    uid = user_id or DEFAULT_USER_ID
    conn = await get_connection()
    try:
        for table in ("messages", "memories", "user_facts", "sent_content",
                      "shared_content", "engagement_state"):
            try:
                await conn.execute(f"DELETE FROM {table} WHERE user_id = ?", (uid,))
            except Exception:
                logger.debug("Reset skipped table %s (may not exist yet)", table, exc_info=True)
        await conn.commit()
        logger.info("Reset all data for user %d", uid)
    finally:
        await conn.close()

    # Clear in-memory per-user state too — otherwise a stuck/old batch task, a
    # held processing lock, or stale counters survive the reset and can block
    # the fresh conversation (the "no reply after reset" bug).
    _cancel_idle(uid)
    nudge_counts.pop(uid, None)
    _openings_started.discard(uid)
    if engine:
        engine.clear_user_state(uid)
    from bot.memory.ltm import clear_retrieval_state
    clear_retrieval_state(uid)

    return {"status": "ok", "user_id": uid}


